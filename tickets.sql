CREATE TABLE Tickets
(
	TicketId INT IDENTITY NOT NULL PRIMARY KEY,
	TicketNumber INT NOT NULL UNIQUE,
	TrainId INT NOT NULL FOREIGN KEY REFERENCES Trains(TrainId),
