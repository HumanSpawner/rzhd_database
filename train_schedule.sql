CREATE TABLE TrainSchedule
(
	ScheduleId INT IDENTITY NOT NULL PRIMARY KEY,
	TrainId INT NOT NULL FOREIGN KEY REFERENCES Trains(TrainId),
	TrainNumber VARCHAR(10) NOT NULL,
	DirectionId INT NOT NULL FOREIGN KEY REFERENCES Stations(StationId),
	DepartureTime TIME NOT NULL,
	ArrivedTime TIME NOT NULL
)
